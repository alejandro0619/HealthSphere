    class BackgroundPanel extends JPanel {
        private Image bg;
        private String path;
        
        public BackgroundPanel(String path) {
            this.path = path;
        }
        
        @Override
        public void paint(Graphics g) {
            bg = new ImageIcon(getClass().getResource(path)).getImage();
            g.drawImage(bg, 0, 0, getWidth(), getHeight(), this);
            setOpaque(false);
            super.paint(g);
        }
    }

